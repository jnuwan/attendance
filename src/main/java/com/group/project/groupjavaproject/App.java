    private void MenuAttendanceActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_MenuAttendanceActionPerformed
        // TODO add your handling code here:
        if (employee != null && employee.getType().equals("Manager")) {
            JInternalFrame managerDashboard = new ManagerDashboard(this.deskPane);
            this.deskPane.add(managerDashboard);
            int x = (deskPane.getWidth() - managerDashboard.getWidth()) / 2;
            int y = (deskPane.getHeight() - managerDashboard.getHeight()) / 4;
            managerDashboard.setBounds(x, y, managerDashboard.getWidth(), managerDashboard.getHeight());
            managerDashboard.setVisible(true);
        } else {
            JOptionPane.showMessageDialog(deskPane, "You have no acceess previlage.");
        }

    }//GEN-LAST:event_MenuAttendanceActionPerformed


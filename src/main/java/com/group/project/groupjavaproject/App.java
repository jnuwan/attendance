    private void btnLogoutActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLogoutActionPerformed
        // TODO add your handling code here:
        Attendance attendance = new Attendance();
        attendance.setEmpId(employee.getId());
        attendance.setType("Logout");
        DateFormat formatter = new SimpleDateFormat("yyyy-MM-dd HH:mm:SS");
        Date date = new Date();
        attendance.setCurrentDateTime(formatter.format(date));
        new AttendanceData().createAttendance(attendance);
        showPannelUser(false);
        JOptionPane.showMessageDialog(deskPane, employee.getEmpId() + " employee logout successfully.");
//        for(int i=0; i<this.deskPane.getComponents().length;i++){
//            deskPane.getComponent(i).
//        }

        employee = null;
    }//GEN-LAST:event_btnLogoutActionPerformed

